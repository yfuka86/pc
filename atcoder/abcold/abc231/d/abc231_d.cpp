int main()
{
  int N, M;
  cin >> N >> M;
  vector<vi> G(N, vi({}));
  UnionFind un = UnionFind(N);
  rep(i, M) {
    int a, b; cin >> a >> b; a--; b--;
    if (find(all(G[a]), b) == G[a].end()) G[a].pb(b);
    if (find(all(G[b]), a) == G[b].end()) G[b].pb(a);
    if (!un.same(a, b)) {
      un.unite(a, b);
    } else {
      cout << "No" << endl;
      return 0;
    }
  }
  rep(i, N) {
    if (G[i].size() > 2) {
      cout << "No" << endl;
      return 0;
    }
  }
  cout << "Yes" << endl;
}

