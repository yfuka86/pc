void solve() {
  int n; cin >> n;
  string s1, s2; cin >> s1 >> s2;
  if (s1 == s2) { cout << "0" << "\n"; return; }
  if (s1 == string(n, '0')) { cout << "-1" << "\n"; return; }

  int diff_count = 0, diff1_count = 0, same1_count = 0;
  rep(i, n) {
    if (s1[i] != s2[i]) {
      diff_count++;
      if (s1[i] == '1') diff1_count++;
    } else {
      if (s1[i] == '1') same1_count++;
    }
  }
  int same_count = n - diff_count;

  // cout << "diff" << diff_count << "same" << same_count << endl;
  int mi = INF;
  if (diff_count % 2 == 0) {
    if (diff_count / 2 == diff1_count) chmin(mi, diff_count);
  }
  if (same_count % 2 == 1) {
    if ((same_count + 1) / 2 == same1_count) chmin(mi, same_count);
  }

  if (mi != INF) { cout << mi << "\n"; } else cout << "-1" << "\n";
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
}
