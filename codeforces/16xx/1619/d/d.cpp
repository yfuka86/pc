void solve() {
  ll m, n;
  cin >> m >> n;
  vector<vector<LP>> people(n, vector<LP>(m, make_pair(0, 0)));
  vector<vl> shop(m, vl(n));
  rep(i, m) {
    rep(j, n) {
      ll t; cin >> t;
      shop[i][j] = t;
      people[j][i] = make_pair(t, i);
    }
  }

  rep(j, n) {
    sort(all(people[j]));
  }
  rep(i, m) {
    sort(all(shop[i]));
  }

  set<ll> s;
  ll mi = LINF;
  rep(j, n) {
    chmin(mi, people[j][m - 1].first);
    s.insert(people[j][m - 1].second);
  }
  if (s.size() < n) { cout << mi << "\n"; return; }

  ll shopma = 0;
  rep(i, m) {
    chmax(shopma, shop[i][n - 2]);
  }
  cout << min(shopma, mi) << "\n";
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
}
